const VEHICLE_CATALOG_STORAGE_KEY = "crm.vehicleCatalog.custom.v1";
const VEHICLE_CATALOG_REMOVED_STORAGE_KEY = "crm.vehicleCatalog.removed.v1";
const VEHICLE_CATALOG_UPDATED_EVENT = "crm:vehicleCatalogUpdated";

export type DynamicVehicleCatalog = Record<string, Record<string, string[]>>;
type RemovedVehicleCatalog = {
  manufacturers: string[];
  models: Record<string, string[]>;
  submodels: Record<string, Record<string, string[]>>;
};

function normalizeText(value: unknown): string {
  return String(value ?? "").trim().replace(/\s+/g, " ");
}

function uniqSorted(values: string[]): string[] {
  return Array.from(new Set(values.map(normalizeText).filter(Boolean))).sort((a, b) => a.localeCompare(b));
}

function cloneCatalog(catalog: DynamicVehicleCatalog): DynamicVehicleCatalog {
  const out: DynamicVehicleCatalog = {};
  Object.entries(catalog || {}).forEach(([manufacturer, models]) => {
    out[manufacturer] = {};
    Object.entries(models || {}).forEach(([model, submodels]) => {
      out[manufacturer][model] = uniqSorted(Array.isArray(submodels) ? submodels : []);
    });
  });
  return out;
}

function emptyRemovedCatalog(): RemovedVehicleCatalog {
  return { manufacturers: [], models: {}, submodels: {} };
}

function cloneRemovedCatalog(removed: Partial<RemovedVehicleCatalog> | null | undefined): RemovedVehicleCatalog {
  const out = emptyRemovedCatalog();
  out.manufacturers = uniqSorted(Array.isArray(removed?.manufacturers) ? removed?.manufacturers ?? [] : []);

  Object.entries(removed?.models || {}).forEach(([manufacturer, models]) => {
    const cleanManufacturer = normalizeText(manufacturer);
    if (!cleanManufacturer) return;
    out.models[cleanManufacturer] = uniqSorted(Array.isArray(models) ? models : []);
  });

  Object.entries(removed?.submodels || {}).forEach(([manufacturer, models]) => {
    const cleanManufacturer = normalizeText(manufacturer);
    if (!cleanManufacturer) return;
    out.submodels[cleanManufacturer] = {};
    Object.entries(models || {}).forEach(([model, submodels]) => {
      const cleanModel = normalizeText(model);
      if (!cleanModel) return;
      out.submodels[cleanManufacturer][cleanModel] = uniqSorted(Array.isArray(submodels) ? submodels : []);
    });
    if (Object.keys(out.submodels[cleanManufacturer]).length === 0) delete out.submodels[cleanManufacturer];
  });

  return out;
}

function getBaseDynamicCatalog(): DynamicVehicleCatalog {
  const out: DynamicVehicleCatalog = {};
  Object.entries(QATAR_VEHICLE_CATALOG).forEach(([manufacturer, models]) => {
    out[manufacturer] = {};
    (models || []).forEach((model) => {
      const cleanModel = normalizeText(model);
      if (cleanModel) out[manufacturer][cleanModel] = [];
    });
  });
  return out;
}

function readCustomCatalog(): DynamicVehicleCatalog {
  if (typeof window === "undefined") return {};
  try {
    const raw = window.localStorage.getItem(VEHICLE_CATALOG_STORAGE_KEY);
    if (!raw) return {};
    const parsed = JSON.parse(raw) as DynamicVehicleCatalog;
    if (!parsed || typeof parsed !== "object" || Array.isArray(parsed)) return {};
    return cloneCatalog(parsed);
  } catch {
    return {};
  }
}

function readRemovedCatalog(): RemovedVehicleCatalog {
  if (typeof window === "undefined") return emptyRemovedCatalog();
  try {
    const raw = window.localStorage.getItem(VEHICLE_CATALOG_REMOVED_STORAGE_KEY);
    if (!raw) return emptyRemovedCatalog();
    const parsed = JSON.parse(raw) as RemovedVehicleCatalog;
    if (!parsed || typeof parsed !== "object" || Array.isArray(parsed)) return emptyRemovedCatalog();
    return cloneRemovedCatalog(parsed);
  } catch {
    return emptyRemovedCatalog();
  }
}

function writeCustomCatalog(catalog: DynamicVehicleCatalog) {
  if (typeof window === "undefined") return;
  try {
    window.localStorage.setItem(VEHICLE_CATALOG_STORAGE_KEY, JSON.stringify(cloneCatalog(catalog)));
    window.dispatchEvent(new CustomEvent(VEHICLE_CATALOG_UPDATED_EVENT));
  } catch {
    // ignore storage/dispatch failures
  }
}

function writeRemovedCatalog(removed: RemovedVehicleCatalog) {
  if (typeof window === "undefined") return;
  try {
    window.localStorage.setItem(VEHICLE_CATALOG_REMOVED_STORAGE_KEY, JSON.stringify(cloneRemovedCatalog(removed)));
    window.dispatchEvent(new CustomEvent(VEHICLE_CATALOG_UPDATED_EVENT));
  } catch {
    // ignore storage/dispatch failures
  }
}

function baseHasManufacturer(manufacturer: string): boolean {
  return Object.prototype.hasOwnProperty.call(QATAR_VEHICLE_CATALOG, manufacturer);
}

function baseHasModel(manufacturer: string, model: string): boolean {
  return Boolean(QATAR_VEHICLE_CATALOG[manufacturer]?.includes(model));
}

function applyRemovedCatalog(catalog: DynamicVehicleCatalog, removed: RemovedVehicleCatalog): DynamicVehicleCatalog {
  const merged = cloneCatalog(catalog);

  removed.manufacturers.forEach((manufacturer) => {
    delete merged[manufacturer];
  });

  Object.entries(removed.models).forEach(([manufacturer, models]) => {
    if (!merged[manufacturer]) return;
    models.forEach((model) => {
      delete merged[manufacturer][model];
    });
  });

  Object.entries(removed.submodels).forEach(([manufacturer, models]) => {
    if (!merged[manufacturer]) return;
    Object.entries(models).forEach(([model, submodels]) => {
      if (!Array.isArray(merged[manufacturer][model])) return;
      merged[manufacturer][model] = merged[manufacturer][model].filter((item) => !submodels.includes(item));
    });
  });

  return merged;
}

function removeFromArray(values: string[], value: string): string[] {
  const cleanValue = normalizeText(value);
  return values.filter((item) => normalizeText(item) !== cleanValue);
}

export function getVehicleCatalogUpdatedEventName(): string {
  return VEHICLE_CATALOG_UPDATED_EVENT;
}

export function getVehicleCatalog(): Record<string, string[]> {
  const base = applyRemovedCatalog(getBaseDynamicCatalog(), readRemovedCatalog());
  const custom = readCustomCatalog();
  const merged = cloneCatalog(base);

  Object.entries(custom).forEach(([manufacturer, models]) => {
    const cleanManufacturer = normalizeText(manufacturer);
    if (!cleanManufacturer) return;
    if (!merged[cleanManufacturer]) merged[cleanManufacturer] = {};

    Object.entries(models || {}).forEach(([model, submodels]) => {
      const cleanModel = normalizeText(model);
      if (!cleanModel) return;
      const existing = merged[cleanManufacturer][cleanModel] ?? [];
      merged[cleanManufacturer][cleanModel] = uniqSorted([...(existing || []), ...((submodels || []) as string[])]);
    });
  });

  const flattened: Record<string, string[]> = {};
  Object.entries(merged).forEach(([manufacturer, models]) => {
    flattened[manufacturer] = Object.keys(models).sort((a, b) => a.localeCompare(b));
  });
  return flattened;
}

export function getManufacturerOptions(): string[] {
  return Object.keys(getVehicleCatalog()).sort((a, b) => a.localeCompare(b));
}

export function getSubmodelsByManufacturerAndModel(manufacturer: string, model: string): string[] {
  const cleanManufacturer = normalizeText(manufacturer);
  const cleanModel = normalizeText(model);
  if (!cleanManufacturer || !cleanModel) return [];

  const catalog = getVehicleCatalogForManagement();
  const submodels = catalog?.[cleanManufacturer]?.[cleanModel] ?? [];
  return uniqSorted(Array.isArray(submodels) ? submodels : []);
}

export function getVehicleCatalogForManagement(): DynamicVehicleCatalog {
  const base = applyRemovedCatalog(getBaseDynamicCatalog(), readRemovedCatalog());
  const custom = readCustomCatalog();
  const merged = cloneCatalog(base);

  Object.entries(custom).forEach(([manufacturer, models]) => {
    const cleanManufacturer = normalizeText(manufacturer);
    if (!cleanManufacturer) return;
    if (!merged[cleanManufacturer]) merged[cleanManufacturer] = {};
    Object.entries(models || {}).forEach(([model, submodels]) => {
      const cleanModel = normalizeText(model);
      if (!cleanModel) return;
      const existing = merged[cleanManufacturer][cleanModel] ?? [];
      merged[cleanManufacturer][cleanModel] = uniqSorted([...(existing || []), ...((submodels || []) as string[])]);
    });
  });

  return merged;
}

export function saveVehicleCatalogEntry(manufacturer: string, model: string, submodel?: string) {
  const cleanManufacturer = normalizeText(manufacturer);
  const cleanModel = normalizeText(model);
  const cleanSubmodel = normalizeText(submodel);
  if (!cleanManufacturer || !cleanModel) return;

  const custom = readCustomCatalog();
  const removed = readRemovedCatalog();
  removed.manufacturers = removeFromArray(removed.manufacturers, cleanManufacturer);
  if (removed.models[cleanManufacturer]) {
    removed.models[cleanManufacturer] = removeFromArray(removed.models[cleanManufacturer], cleanModel);
    if (removed.models[cleanManufacturer].length === 0) delete removed.models[cleanManufacturer];
  }
  if (cleanSubmodel && removed.submodels[cleanManufacturer]?.[cleanModel]) {
    removed.submodels[cleanManufacturer][cleanModel] = removeFromArray(
      removed.submodels[cleanManufacturer][cleanModel],
      cleanSubmodel
    );
    if (removed.submodels[cleanManufacturer][cleanModel].length === 0) {
      delete removed.submodels[cleanManufacturer][cleanModel];
    }
    if (Object.keys(removed.submodels[cleanManufacturer]).length === 0) delete removed.submodels[cleanManufacturer];
  }

  if (!custom[cleanManufacturer]) custom[cleanManufacturer] = {};
  const current = custom[cleanManufacturer][cleanModel] ?? [];
  custom[cleanManufacturer][cleanModel] = cleanSubmodel ? uniqSorted([...current, cleanSubmodel]) : uniqSorted(current);
  writeRemovedCatalog(removed);
  writeCustomCatalog(custom);
}

export function removeVehicleManufacturer(manufacturer: string) {
  const cleanManufacturer = normalizeText(manufacturer);
  if (!cleanManufacturer) return;
  const custom = readCustomCatalog();
  const removed = readRemovedCatalog();
  delete custom[cleanManufacturer];
  if (baseHasManufacturer(cleanManufacturer)) {
    removed.manufacturers = uniqSorted([...removed.manufacturers, cleanManufacturer]);
  }
  delete removed.models[cleanManufacturer];
  delete removed.submodels[cleanManufacturer];
  writeRemovedCatalog(removed);
  writeCustomCatalog(custom);
}

export function removeVehicleModel(manufacturer: string, model: string) {
  const cleanManufacturer = normalizeText(manufacturer);
  const cleanModel = normalizeText(model);
  if (!cleanManufacturer || !cleanModel) return;
  const custom = readCustomCatalog();
  const removed = readRemovedCatalog();
  if (custom[cleanManufacturer]) {
    delete custom[cleanManufacturer][cleanModel];
    if (Object.keys(custom[cleanManufacturer]).length === 0) delete custom[cleanManufacturer];
  }
  if (baseHasModel(cleanManufacturer, cleanModel)) {
    removed.models[cleanManufacturer] = uniqSorted([...(removed.models[cleanManufacturer] ?? []), cleanModel]);
  }
  if (removed.submodels[cleanManufacturer]) {
    delete removed.submodels[cleanManufacturer][cleanModel];
    if (Object.keys(removed.submodels[cleanManufacturer]).length === 0) delete removed.submodels[cleanManufacturer];
  }
  writeRemovedCatalog(removed);
  writeCustomCatalog(custom);
}

export function removeVehicleSubmodel(manufacturer: string, model: string, submodel: string) {
  const cleanManufacturer = normalizeText(manufacturer);
  const cleanModel = normalizeText(model);
  const cleanSubmodel = normalizeText(submodel);
  if (!cleanManufacturer || !cleanModel || !cleanSubmodel) return;
  const custom = readCustomCatalog();
  const removed = readRemovedCatalog();
  const existing = custom?.[cleanManufacturer]?.[cleanModel];
  if (Array.isArray(existing) && existing.length > 0) {
    custom[cleanManufacturer][cleanModel] = existing.filter((item) => normalizeText(item) !== cleanSubmodel);
  }
  const fullCatalog = getVehicleCatalogForManagement();
  if (fullCatalog?.[cleanManufacturer]?.[cleanModel]?.includes(cleanSubmodel)) {
    if (!removed.submodels[cleanManufacturer]) removed.submodels[cleanManufacturer] = {};
    removed.submodels[cleanManufacturer][cleanModel] = uniqSorted([
      ...(removed.submodels[cleanManufacturer][cleanModel] ?? []),
      cleanSubmodel,
    ]);
  }
  writeRemovedCatalog(removed);
  writeCustomCatalog(custom);
}

export function renameVehicleManufacturer(manufacturer: string, nextManufacturer: string) {
  const cleanManufacturer = normalizeText(manufacturer);
  const cleanNextManufacturer = normalizeText(nextManufacturer);
  if (!cleanManufacturer || !cleanNextManufacturer || cleanManufacturer === cleanNextManufacturer) return;

  const catalog = getVehicleCatalogForManagement();
  const models = catalog[cleanManufacturer] ?? {};
  removeVehicleManufacturer(cleanManufacturer);
  Object.entries(models).forEach(([model, submodels]) => {
    if (submodels.length === 0) {
      saveVehicleCatalogEntry(cleanNextManufacturer, model);
      return;
    }
    submodels.forEach((submodel) => saveVehicleCatalogEntry(cleanNextManufacturer, model, submodel));
  });
}

export function renameVehicleModel(manufacturer: string, model: string, nextModel: string) {
  const cleanManufacturer = normalizeText(manufacturer);
  const cleanModel = normalizeText(model);
  const cleanNextModel = normalizeText(nextModel);
  if (!cleanManufacturer || !cleanModel || !cleanNextModel || cleanModel === cleanNextModel) return;

  const submodels = getVehicleCatalogForManagement()?.[cleanManufacturer]?.[cleanModel] ?? [];
  removeVehicleModel(cleanManufacturer, cleanModel);
  if (submodels.length === 0) {
    saveVehicleCatalogEntry(cleanManufacturer, cleanNextModel);
    return;
  }
  submodels.forEach((submodel) => saveVehicleCatalogEntry(cleanManufacturer, cleanNextModel, submodel));
}

export function renameVehicleSubmodel(manufacturer: string, model: string, submodel: string, nextSubmodel: string) {
  const cleanManufacturer = normalizeText(manufacturer);
  const cleanModel = normalizeText(model);
  const cleanSubmodel = normalizeText(submodel);
  const cleanNextSubmodel = normalizeText(nextSubmodel);
  if (!cleanManufacturer || !cleanModel || !cleanSubmodel || !cleanNextSubmodel || cleanSubmodel === cleanNextSubmodel) return;

  removeVehicleSubmodel(cleanManufacturer, cleanModel, cleanSubmodel);
  saveVehicleCatalogEntry(cleanManufacturer, cleanModel, cleanNextSubmodel);
}
